import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IDdBookPersonFt } from '@/shared/model/dd-book-person-ft.model';

const baseApiUrl = 'api/dd-book-people';

export default class DdBookPersonFtService {
  public find(id: number): Promise<IDdBookPersonFt> {
    return new Promise<IDdBookPersonFt>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IDdBookPersonFt): Promise<IDdBookPersonFt> {
    return new Promise<IDdBookPersonFt>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IDdBookPersonFt): Promise<IDdBookPersonFt> {
    return new Promise<IDdBookPersonFt>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
